package hdu.cn.hziee.model;

public class FoodFormula {
    private Integer ffId;

    private Integer recipesId;

    private Integer foodId;

    private Double foodNumber;

    public Integer getFfId() {
        return ffId;
    }

    public void setFfId(Integer ffId) {
        this.ffId = ffId;
    }

    public Integer getRecipesId() {
        return recipesId;
    }

    public void setRecipesId(Integer recipesId) {
        this.recipesId = recipesId;
    }

    public Integer getFoodId() {
        return foodId;
    }

    public void setFoodId(Integer foodId) {
        this.foodId = foodId;
    }

    public Double getFoodNumber() {
        return foodNumber;
    }

    public void setFoodNumber(Double foodNumber) {
        this.foodNumber = foodNumber;
    }
}