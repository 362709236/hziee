package hdu.cn.hziee.model;

public class SetMeal {
    private Integer smNumber;

    private Integer smId;

    private Integer recipesId;

    public Integer getSmNumber() {
        return smNumber;
    }

    public void setSmNumber(Integer smNumber) {
        this.smNumber = smNumber;
    }

    public Integer getSmId() {
        return smId;
    }

    public void setSmId(Integer smId) {
        this.smId = smId;
    }

    public Integer getRecipesId() {
        return recipesId;
    }

    public void setRecipesId(Integer recipesId) {
        this.recipesId = recipesId;
    }
}